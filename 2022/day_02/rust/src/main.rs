fn main() {
    let out = include_bytes!("../../input.txt")
        .split(|b| *b == 10)
        .fold((0, 0), |(a1, a2), j| match j {
            [65, _, 89] => (8 + a1, 4 + a2),
            [66, _, 90] => (9 + a1, 9 + a2),
            [67, _, 88] => (7 + a1, 2 + a2),
            [65, _, 88] => (4 + a1, 3 + a2),
            [66, _, 89] => (5 + a1, 5 + a2),
            [67, _, 90] => (6 + a1, 7 + a2),
            [65, _, 90] => (3 + a1, 8 + a2),
            [66, _, 88] => (1 + a1, 1 + a2),
            [67, _, 89] => (2 + a1, 6 + a2),
            _ => (a1, a2),
        });
    println!("{}", out.0);
    println!("{}", out.1);
}

